    private void srediFormu() {
        setTitle("Detalji termina");
        cmbPacijent.getModel().setSelectedItem(t.getPacijent());
        cmbPacijent.setEnabled(false);
        cmbDoktor.getModel().setSelectedItem(t.getDoktor());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
        txtDatumVreme.setText(sdf.format(t.getDatumVreme()));
        txtCenaTermina.setText(String.valueOf(t.getCenaTermina()));
        txtCenaTermina.setEnabled(false);
        popuniDoktore();
        popuniUsluge();
        tblStavkeTermina.setModel(new TableModelStavkeTermina(t));
        lblObjasnjenje.setText("Termin moze biti zakazan samo na ceo sat!");

        if (t.getDatumVreme().before(new Date())
                || t.getDatumVreme().equals(new Date())) {
            lblObavestenje.setText("Ovaj termin je prosao, ne mozete ga menjati niti otkazivati!");
            btnObrisi.setEnabled(false);
            btnIzmeni.setEnabled(false);
        }

    }

    private void popuniDoktore() {
        try {
            ArrayList<Doktor> doktori = ClientController.getInstance().getAllDoktor();

            cmbDoktor.removeAllItems();

            for (Doktor doktor : doktori) {
                cmbDoktor.addItem(doktor);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniUsluge() {
        try {
            ArrayList<Usluga> usluge = ClientController.getInstance().getAllUsluga();

            cmbUsluga.removeAllItems();

            for (Usluga usluga : usluge) {
                cmbUsluga.addItem(usluga);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


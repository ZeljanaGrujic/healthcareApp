    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {
            Doktor doktor = (Doktor) cmbDoktor.getSelectedItem();

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy HH:mm");
            Date datumVreme = sdf.parse(txtDatumVreme.getText());
            
            if (datumVreme.before(new Date()) 
                    || datumVreme.equals(new Date())) {
              
                 JOptionPane.showMessageDialog(this, "Termin mora biti rezervisan nakon danasnjeg datuma!", "Upozorenje",JOptionPane.WARNING_MESSAGE);
                return;
            }

            Calendar cal = Calendar.getInstance();
            cal.setTime(datumVreme);

            if (cal.get(Calendar.MINUTE) != 0) {
              
                  JOptionPane.showMessageDialog(this, "Termin moze biti zakazan samo na ceo sat!", "Upozorenje",JOptionPane.WARNING_MESSAGE);

                return;
            }

            double cenaTermina = Double.parseDouble(txtCenaTermina.getText());

            TableModelStavkeTermina tm = (TableModelStavkeTermina) tblStavkeTermina.getModel();

            t.setDoktor(doktor);
            t.setDatumVreme(datumVreme);
            t.setCenaTermina(cenaTermina);
            t.setStavkeTermina(tm.getLista());

            ClientController.getInstance().updateTermin(t);
            FormPretragaTermina fp = (FormPretragaTermina) getParent();
            fp.refreshTable();
            
              JOptionPane.showMessageDialog(this, "Uspesno ste izmenili termin.", "Obavestenje",JOptionPane.INFORMATION_MESSAGE);

            this.dispose();

        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiTermina.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "otkazete ovaj termin?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteTermin(t);
                FormPretragaTermina fp = (FormPretragaTermina) getParent();
                fp.refreshTable();
                
                  JOptionPane.showMessageDialog(this, "Uspesno ste otkazali termin.", "Obavestenje",JOptionPane.INFORMATION_MESSAGE);

                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiTermina.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnObrisiUsluguActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiUsluguActionPerformed

        int row = tblStavkeTermina.getSelectedRow();

        if (row >= 0) {
            TableModelStavkeTermina tm = (TableModelStavkeTermina) tblStavkeTermina.getModel();

            StavkaTermina st = tm.getSelectedStavka(row);

            double cenaTermina = Double.parseDouble(txtCenaTermina.getText());
            txtCenaTermina.setText(String.valueOf(cenaTermina - st.getCenaStavke()));

            tm.obrisiStavku(row);
        }
    }//GEN-LAST:event_btnObrisiUsluguActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        Usluga u = (Usluga) cmbUsluga.getSelectedItem();

        TableModelStavkeTermina tm = (TableModelStavkeTermina) tblStavkeTermina.getModel();
        if (tm.postojiUsluga(u)) {
          
              JOptionPane.showMessageDialog(this, "Vec ste uneli tu uslugu za ovaj termin!", "Greska",JOptionPane.ERROR_MESSAGE);

            return;
        }

        double cena;

        if (txtCenaUsluge.getText().isEmpty()) {
            cena = u.getCena();
        } else {
            cena = Double.parseDouble(txtCenaUsluge.getText());
        }

        if (cena < 500 || cena > 1000000) {
            //JOptionPane.showMessageDialog(this, "Cena usluge mora biti izmedju 500 i 1000000!");
              JOptionPane.showMessageDialog(this, "Cena usluge mora biti izmedju 500 i 1000000!", "Upozorenje",JOptionPane.WARNING_MESSAGE);

            return;
        }

        StavkaTermina st = new StavkaTermina(t, -1, cena, u);

        tm.dodajStavku(st);

        double cenaTermina = Double.parseDouble(txtCenaTermina.getText());
        txtCenaTermina.setText(String.valueOf(cenaTermina + st.getCenaStavke()));

    }//GEN-LAST:event_btnDodajActionPerformed


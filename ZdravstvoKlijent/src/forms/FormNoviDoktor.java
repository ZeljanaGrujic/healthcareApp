    private void btnZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnZapamtiActionPerformed

        try {
            if (txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty()
                    || txtKorisnickoIme.getText().isEmpty() || txtPrezime.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti sve podatke za novog doktora", "Upozorenje", JOptionPane.WARNING_MESSAGE);
                return;
            }
            Organizacija org = (Organizacija) cmbZdrOrganizacije.getSelectedItem();

            String ime = txtIme.getText();
            String prezime = txtPrezime.getText();
            String username = txtKorisnickoIme.getText();
            String password = new String(txtLozinka.getPassword());

            Doktor d = new Doktor(null, ime, prezime, username, password, org);

            ClientController.getInstance().addDoktor(d);

            MainForm mf = (MainForm) this.getParent();
            mf.popuniDoktore();

            JOptionPane.showMessageDialog(this, "Uspesno dodat doktor", "Potvrda", JOptionPane.INFORMATION_MESSAGE);

            this.dispose();
        } catch (Exception ex) {
            // Logger.getLogger(FormNoviDoktor.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Neuspesno dodat doktor", "Greska", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnZapamtiActionPerformed


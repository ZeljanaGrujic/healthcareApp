    private void popuniZdravstveneOrg() {

        try {
            ArrayList<Organizacija> listaOrg = ClientController.getInstance().getAllOrganizacija();
            cmbZdrOrganizacije.removeAllItems();

            for (Organizacija organizacija : listaOrg) {
                cmbZdrOrganizacije.addItem(organizacija);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormNoviDoktor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
